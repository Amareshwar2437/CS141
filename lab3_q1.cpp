// Library
#include <iostream>
using namespace std;

// To declare the main function
int main() {

           // Declaring variables of all types

           int i;

           float a;

           double b;

           char c;

           bool d;
// Now we have to initialize each one of them

	i=24;
	a=6.7;
	b=3.145;
	c='X';
	d=true;
// Now we have to get output of all the values

	cout << i << endl;

	cout << a << endl;

	cout << b << endl;

	cout << c << endl;

	cout << d << endl;
//Now we display the size of all variables
	cout<<"Size of i ="<<sizeof(i)<<endl;

	cout<<"Size of a ="<<sizeof(a)<<endl;

	cout<<"Size of b ="<<sizeof(b)<<endl;

	cout<<"Size of c ="<<sizeof(c)<<endl;

	cout<<"Size of d ="<<sizeof(d)<<endl;
return 0;
}
	
